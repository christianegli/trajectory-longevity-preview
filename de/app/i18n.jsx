  const dict = dicts[lang];
  if (!dict) return;

  function localizeTextNode(node) {
    const raw = node.nodeValue || "";
    const trimmed = raw.trim();
    if (!trimmed) return;
    if (dict[trimmed]) {
      node.nodeValue = raw.replace(trimmed, dict[trimmed]);
      return;
    }
    let next = raw;
    Object.entries(dict)
      .filter(([from]) => from.length > 2)
      .sort((a, b) => b[0].length - a[0].length)
      .forEach(([from, to]) => {
        if (next.includes(from)) next = next.split(from).join(to);
      });
    if (next !== raw) node.nodeValue = next;
  }

  function walk(root) {
    const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
      acceptNode(node) {
        const parent = node.parentElement;
        if (!parent || parent.closest(".top") || /^(SCRIPT|STYLE|TEXTAREA|INPUT)$/i.test(parent.tagName)) return NodeFilter.FILTER_REJECT;
        return NodeFilter.FILTER_ACCEPT;
      }
    });
    const nodes = [];
    while (walker.nextNode()) nodes.push(walker.currentNode);
    nodes.forEach(localizeTextNode);
    document.querySelectorAll("input[placeholder]").forEach(input => {
      const p = input.getAttribute("placeholder");
      if (p && p.startsWith("Search ")) input.setAttribute("placeholder", lang === "de" ? p.replace("Search", "Suche") : lang === "fr" ? p.replace("Search", "Rechercher") : p.replace("Search", "Buscar"));
    });
  }

